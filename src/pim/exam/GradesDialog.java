    private void jButtonSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSubmitActionPerformed
        if (jComboBoxGrade.getSelectedIndex() == 0) {
            jComboBoxGrade.requestFocus();
        } else {
            exam.setGrade(Exam.GRADES[jComboBoxGrade.getSelectedIndex() - 1]);
            changed = true;
            this.dispose();
        }
    }//GEN-LAST:event_jButtonSubmitActionPerformed

    private void jTextFieldKeyReleased(java.awt.event.KeyEvent evt) {
        exam.setNumbers(getNumbers());
        int students = exam.getStudents();
        if (students > 0) {
            jLabelStudents.setText(students + "");
            double average = exam.getAverage();
            if (average > 0) {
                jLabelAverage.setText(average + "");
            } else {
                jLabelAverage.setText("-");
            }
        } else {
            exam.setNumbers(null);
            jLabelStudents.setText("-");
            jLabelAverage.setText("-");
        }
    }

    public Exam getExam() {
        if (changed) {
            return exam; 
        } else {
            exam.setNumbers(oldnumbers);
            return null;
        }
    }

